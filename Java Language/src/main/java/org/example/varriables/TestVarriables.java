package org.example.varriables;

public class TestVarriables {
    public  static void main(String[] args) {

        // primitive data type

        int num = 3;
        System.out.println(num);

        // type nameOfVarriable = value;
        int num1 = 10;
        int num2 = 20;
        int result = num1 + num2;
        System.out.println(result);
        short num3 = 1;
        long num4 = 2L;
        byte num5 = 127;

        //float

        float val = 3.17f;
        System.out.println(val);

        double val1 = 3.16;
        System.out.println(val1);

        // characters

        char ch = 'a';
        System.out.println(ch);

        // boolean

        boolean bool = true;
        System.out.println(bool);


    }
}
